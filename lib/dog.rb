class Dog
   def initialize
      puts "Create an animal"
   end
   
   def set_name(new_name)
      @name = new_name
   end

   def name
      @name
   end

   def name=(new_name)
      @name = new_name
   end
   
   def set_breed(new_breed)
      @breed = new_breed
   end

   def breed
      @breed
   end

   def breed=(new_breed)
      @breed = new_breed
   end
   
end

